// Function should return the length of the loop in LL.

class Solution {
    // Function to find the length of a loop in the linked list.
    public int countNodesinLoop(Node head) {
       Node temp = head;
       Node ptr = head;
       int count =0;
       
       while(ptr!=null){
           temp = temp.next;
           if(ptr.next==null){
               return 0;
           }
           ptr = ptr.next.next;
           if(ptr==temp){
               //System.out.println(ptr.data);
               Node s = ptr;
               Node point = null;
              while(head!=s){
                  head =head.next;
                  s = s.next;
              }
               point = s;
               
               count = 1;
               Node rt = point;
               point = point.next;
              while(point!=rt){
                  count++;
                  point = point.next;
              }
               return count ;
           }
       }
       
       return count;
    }
}